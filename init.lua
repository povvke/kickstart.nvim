-- Lazy-load `lua_ls` setup only when opening Lua files
vim.api.nvim_create_autocmd("FileType", {
  pattern = "lua",
  callback = function()
    require("lspconfig").lua_ls.setup {
      on_init = function(client)
        if client.workspace_folders then
          local path = client.workspace_folders[1].name
          if vim.uv.fs_stat(path .. '/.luarc.json') or vim.uv.fs_stat(path .. '/.luarc.jsonc') then
            return
          end
        end

        client.config.settings.Lua = vim.tbl_deep_extend('force', client.config.settings.Lua, {
          runtime = {
            version = 'LuaJIT'
          },
          workspace = {
            checkThirdParty = false,
            library = {
              vim.env.VIMRUNTIME
            }
          }
        })
      end,
      settings = {
        Lua = {}
      }
    }
  end,
})

-- Lazy-load 'astro-lsp'
vim.api.nvim_create_autocmd("FileType", {
  pattern = "astro",
  callback = function()
    require("lspconfig").astro.setup {
      autostart = false,
      on_init = function(client)
        local orig_rpc_request = client.rpc.request

        -- fucking make fucking shit work as snippets fukc
        function client.rpc.request(method, params, handler, ...)
          local orig_handler = handler

          if method == 'textDocument/completion' then
            handler = function(...)
              local err, result = ...
              if not err and result then
                local items = result.items or result
                for _, item in ipairs(items) do
                  if item.label then
                    item.insertTextFormat = require('vim.lsp').protocol.InsertTextFormat.Snippet
                  end
                end
              end
              return orig_handler(...)
            end
          end
          return orig_rpc_request(method, params, handler, ...)
        end
      end,
    }
  end
})


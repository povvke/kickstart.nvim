-- Lazy load each lsp
for server, filetype in pairs(servers) do
  vim.api.nvim_create_autocmd("FileType", {
    pattern = filetype,
    callback = function()
      local lspconfig = require("lspconfig")
      if not lspconfig[server].is_initialized then
        lspconfig[server].setup({ autostart = false })
      end
    end
  })
end



